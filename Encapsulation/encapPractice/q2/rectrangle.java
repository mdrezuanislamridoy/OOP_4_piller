package OOP4Piller.Encapsulation.encapPractice.q2;

public class rectrangle {
    private double height;
    private double width;

    public void setH(double height){
        this.height=height;
    }
    public void setW(double width){
        this.width=width;
    }

    public double getH(){
        return height;
    }
    public double getW(){
        return width;
    }

   
}
